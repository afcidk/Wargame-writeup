#include <stdio.h>
#include <string.h>

char data[] = {0x8F, 0xAA, 0x85, 0xA0, 0x48, 0xAC, 0x40, 0x95, 0xB6, 0x16, 0xBE, 0x40, 0xB4, 0x16, 0x97, 0xB1, 0xBE, 0xBC, 0x16, 0xB1, 0xBC, 0x16, 0x9D, 0x95, 0xBC, 0x41, 0x16, 0x36, 0x42, 0x95, 0x95, 0x16, 0x40, 0xB1, 0xBE, 0xB2, 0x16, 0x36, 0x42, 0x3d, 0x3d, 0x49};

int main() {
    for (int i=0; i<strlen(data); ++i) {
        char a = (data[i]&0xaa) >> 1;
        char b = 2 * (data[i] & 0x55);
        a |= b;
        a -= 9;
        printf("%c", a);
    }
    printf("\n");
    return 0;
}
